    event = "BufRead",
    branch = 'v2',
    config = function()
      require("hop").setup {}
      vim.api.nvim_set_keymap("n", "t", ":HopChar2<cr>", { silent = true })
      vim.api.nvim_set_keymap("n", "J", ":HopLine<cr>", { silent = true })
      vim.api.nvim_set_keymap("n", "f", ":HopWord<cr>", { silent = true })
    end,
  },
  {
    -- markdown浏览器预览
